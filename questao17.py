raio = float(input("Digite o valor do raio: "))
pi = 3.14
comprimento_esfera = 2 * pi * raio
area_esfera = pi * raio ** 2
volume_esfera = 3 / 4 * pi * raio ** 3

print("O comprimento da esfera é {} \nA área da esfera é {} \nO volume da esfera é {}".format(comprimento_esfera, area_esfera, volume_esfera))
